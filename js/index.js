let placeNode = document.querySelector(`.places`);
let x1y2 = placeNode.querySelector(`#x1y2`);
let placeCountNode = document.querySelector(`#place-count`);
let priceNode = document.querySelector(`#price`);
let ThankNode = document.querySelector(`.Thank`);
let confNode = document.querySelector(`.conf`);
let placesCounter = 0;
let price = 0;
let counter = 0;



x1y2.addEventListener(`click`, function () {
    counter += 1;
    if (counter % 2 != 0){
        x1y2.classList.add(`active`);
        placesCounter += 1;
        price += 500;
        placeCountNode.innerHTML = placesCounter;
        priceNode.innerHTML = price;
    }
    else {
        x1y2.classList.remove(`active`);
        placesCounter -= 1;
        price -= 500;
        placeCountNode.innerHTML = placesCounter;
        priceNode.innerHTML = price;
    }
})

